import {defineComponent, ref, onMounted, computed} from 'vue'
import { getMeetup } from './meetupsService.ts'

export default defineComponent({
  name: 'SelectedMeetupApp',

  setup() {
    const meetupId = ref(1);
    const meetups = ref({});

    async function getMet(id){
      meetups.value = await getMeetup(id);
    }

    const meetupTitle = computed( ()=>{
      return meetups.value.title;
    })

    onMounted(async()=>{
        await getMet(meetupId.value)
      }
    )
    return {
      meetupId,
      meetupTitle,
      getMet,
    }
  },

  template: `
    <div class="meetup-selector">
      <div class="meetup-selector__control">
        <button class="button button--secondary" type="button" :disabled="Number(meetupId) === 1" @click="getMet(--meetupId)">Предыдущий</button>

        <div class="radio-group" role="radiogroup">
          <div class="radio-group__button">
            <input
              id="meetup-id-1"
              class="radio-group__input"
              type="radio"
              name="meetupId"
              v-model="meetupId"
              value="1" @change="getMet(meetupId)"
            />
            <label for="meetup-id-1" class="radio-group__label">1</label>
          </div>
          <div class="radio-group__button">
            <input
              id="meetup-id-2"
              class="radio-group__input"
              type="radio"
              name="meetupId"
              v-model="meetupId"
              value="2" @change="getMet(meetupId)"
            />
            <label for="meetup-id-2" class="radio-group__label">2</label>
          </div>
          <div class="radio-group__button">
            <input
              id="meetup-id-3"
              class="radio-group__input"
              type="radio"
              name="meetupId"
              v-model="meetupId"
              value="3" @change="getMet(meetupId)"
            />
            <label for="meetup-id-3" class="radio-group__label">3</label>
          </div>
          <div class="radio-group__button">
            <input
              id="meetup-id-4"
              class="radio-group__input"
              type="radio"
              name="meetupId"
              v-model="meetupId"
              value="4" @change="getMet(meetupId)"
            />
            <label for="meetup-id-4" class="radio-group__label">4</label>
          </div>
          <div class="radio-group__button">
            <input
              id="meetup-id-5"
              class="radio-group__input"
              type="radio"
              name="meetupId"
              v-model="meetupId"
              value="5" @change="getMet(meetupId)"
            />
            <label for="meetup-id-5" class="radio-group__label">5</label>
          </div>
        </div>

        <button class="button button--secondary" type="button" :disabled="Number(meetupId) === 5" @click="getMet(++meetupId)">Следующий</button>
      </div>

      <div class="meetup-selector__cover">
        <div class="meetup-cover">
          <h1 class="meetup-cover__title">{{ meetupTitle }}</h1>
        </div>
      </div>

    </div>
  `,
})
